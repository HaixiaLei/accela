#import "UIFont+Helper.h"

@implementation UIFont (Helper)


+ (UIFont *)heitiLightWithSize:(CGFloat)fontSize
{
    UIFont *font = [UIFont fontWithName:@"STHeitiSC-Light" size:fontSize];
    return font;
}

+ (UIFont *)heitiMediumWithSize:(CGFloat)fontSize
{
    UIFont *font = [UIFont fontWithName:@"STHeitiSC-Medium" size:fontSize];
    return font;
}
@end
