#import "NoticeListObject.h"

@implementation NoticeListObject
@synthesize idd;
@synthesize sendday;
@synthesize subject;

- (id)initWithAttribute:(NSDictionary *) attribute
{
    if (self = [super init])
    {
        self.idd = [attribute objectForKey:@"id"];
        self.sendday = [attribute objectForKey:@"sendday"];
        self.subject = [attribute objectForKey:@"subject"];
    }
    return self;
}
@end
