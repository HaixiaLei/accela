        self.availablebalance = [attribute objectForKey:@"availablebalance"];
        self.cntitle = [attribute objectForKey:@"cntitle"];
        self.description = [attribute objectForKey:@"description"];
        self.entitle = [attribute objectForKey:@"entitle"];
        self.entry = [attribute objectForKey:@"entry"];
        self.fromuserid = [attribute objectForKey:@"fromuserid"];
        self.operations = [attribute objectForKey:@"operations"];
        self.orderno = [attribute objectForKey:@"orderno"];
        self.ordertypeid = [attribute objectForKey:@"ordertypeid"];
        self.preavailable = [attribute objectForKey:@"preavailable"];
        self.times = [attribute objectForKey:@"times"];
        self.title = [attribute objectForKey:@"title"];
        self.transferstatus = [attribute objectForKey:@"transferstatus"];
        self.uniquekey = [attribute objectForKey:@"uniquekey"];
        self.username = [attribute objectForKey:@"username"];
    }
    return self;
}
@end
